package interwiewday01;

public class C05_InterviewQuestion {
    public static void main(String[] args) {

        /*
    Tüm alfabeyi loop kullanarak yazdiriniz
    print all alphabet using for loop program in java
     */
      //for  döngoleri char ile de çalışır askiye göre dönüşür

        for (char i = 'a'; i < 'z' ; i++) {
            System.out.print(i);

        }//char ile oluşturdu

        for (char ch = 'a'; ch <= 'z'; ch++) {
            System.out.print( ch +" ");
        }




    }
}
